public class DataTypes {
    /**
     * @param args
     */
    public static void main (String args[]) {
        byte b = 8;
        System.out.println(b);

        // Only Single Character DataTypes
        char ch = 'C';
        System.out.println(ch);

        // Boolean Dataypes
        boolean bool = true;
        System.out.println(bool);

        // int Datatypes 
        int var = 5;
        System.out.println(var);

        // long Datatypes used to store biggest integer Value (Only 9 digits).
        long lon = 999999999;
        System.out.println(lon);

        // float Datatypes 
        float flt = (float) 10.0000888888;
        System.out.println(flt);

        // double Datatypes used to store biggest Float Value.
        double dob = 10.43553344;
        System.out.println(dob);

        // Short Datatypes 
        short st = 240;
        System.out.println(st);
    
    }
}
